import string
import random

def generate_random_password(length=12):
    """
    Generates a random password ensuring it contains a mix of lowercase letters,
    uppercase letters, digits, and punctuation.
    The password length must be at least 4 to guarantee all character types.
    If length is less than 4, it will attempt to include as many types as possible.
    """
    if length < 4:
        # For very short passwords, it's hard to guarantee all types.
        # One option is to raise ValueError, another is to do best effort.
        # Current "best effort": use the old simpler method or just a subset.
        # For this implementation, let's ensure it doesn't crash and tries to be diverse.
        # This edge case might need specific requirements if length < 4 is common.
        # Assuming length >= 4 for guaranteed diversity as per typical password policies.
        # If length is very small, the "guarantee" part might be relaxed or characters chosen carefully.
        # For now, if length < 4, we'll just use the original simpler method for those few chars.
        # Or, more simply, one could argue a password of length < 4 shouldn't be generated by this function
        # if strict complexity is always required. Let's enforce minimum length for guarantee.
        if length == 0:
            return ""
        if length == 1: # Only one type possible
            return random.choice(string.ascii_letters + string.digits + string.punctuation)
        # For 2, 3, we can't guarantee all 4 types.
        # This simplified fallback will just pick from the combined pool.
        # A more robust solution for length < 4 would pick from distinct pools up to length.
        characters = string.ascii_letters + string.digits + string.punctuation
        return ''.join(random.choice(characters) for _ in range(length))


    # Define character sets
    lower = string.ascii_lowercase
    upper = string.ascii_uppercase
    digits = string.digits
    punct = string.punctuation

    # Start password with one of each required type
    password_chars = [
        random.choice(lower),
        random.choice(upper),
        random.choice(digits),
        random.choice(punct),
    ]

    # If length is greater than 4, fill the rest with random choices from all sets
    all_chars = lower + upper + digits + punct
    remaining_length = length - 4
    password_chars.extend(random.choice(all_chars) for _ in range(remaining_length))

    # Shuffle the list to ensure the order is random
    random.shuffle(password_chars)

    return "".join(password_chars)

