def generate_random_username(email_prefix, length=8, max_attempts=10):
    """
    Generates a random username using an email prefix and checks for uniqueness.

    Args:
        email_prefix (str): The part of the email before the '@' symbol.
        length (int): The desired length of the random part of the username.
        max_attempts (int): Maximum attempts to find a unique username.

    Returns:
        str: A unique username, or None if a unique username cannot be generated.
    """
    # Ensure the base prefix is not too long
    # Django's User model username max_length is 150 by default.
    # We take a portion of the email_prefix and append random chars.
    # Max length for prefix part should be username_max_length - length_of_random_part - 1 (for potential underscore)
    # However, let's keep it simpler: ensure email_prefix isn't excessively long.
    # A more robust solution might truncate email_prefix if it's too long.
    # For now, we'll assume email_prefix is of reasonable length.

    if length > 150: # Django's default User.username max_length
        length = 150


    for attempt in range(max_attempts):
        # Take a part of the email prefix. Let's say up to 10 chars, or less if length is small
        prefix_len_to_use = min(len(email_prefix), 150 - length -1) # -1 for a potential separator if we added one
        if prefix_len_to_use < 0: # This case happens if length is already 150
            prefix_len_to_use = 0


        username_candidate = email_prefix[:prefix_len_to_use]

        # If the prefix part itself is already long, adjust random part length
        current_length_of_random_part = length
        if len(username_candidate) + current_length_of_random_part > 150:
            current_length_of_random_part = 150 - len(username_candidate)

        if current_length_of_random_part < 0: # Should not happen if previous logic is correct
             current_length_of_random_part = 0


        random_suffix = ''.join(random.choices(string.ascii_lowercase + string.digits, k=current_length_of_random_part))
        username_candidate += random_suffix

        # Ensure the final username doesn't exceed 150 chars (should be handled by above logic, but as a safeguard)
        username_candidate = username_candidate[:150]


        if not User.objects.filter(username=username_candidate).exists():
            return username_candidate

    # Fallback or error logging
    # For now, returning None and letting the caller handle it.
    # Consider logging a warning here in a real application.
    # logger.warning(f"Could not generate unique username for {email_prefix} after {max_attempts} attempts.")
    return None
